package Variables

fun main() {
    /*
    ALL TYPES

    + = sum
    - = subtraction
    * = multiply
    / = division
    % = modulus
     */

    var age1 = 31
    var age2 = 35

    println(age1 + age2)
    println(age1 - age2)
    println(age1 * age2)
    println(age1 / age2)
    println(age1 % age2)
    println(++age1)





    println()
}