If you’re already familiar with the concept of objects,
it’s important to note that val does not make objects immutable.
In other words, you can’t put something new in that bucket,
but what’s in that bucket could still change itself in some way.
That’s why Kotlin programmers tend to call variables declared with val
“read-only” instead of “immutable”.
*/
